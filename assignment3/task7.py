# 1. Даны числа X, Y, Z, T — длины сторон четырехугольника. 
# Вычислите его площадь, если угол между сторонами длиной X и Y является прямым. 
# Используйте две процедуры для вычисления площадей: прямоугольного треугольника и прямоугольника.

# 2. Напишите программу, 
# которая преобразует заданное ей неотрицательное целое число в 10-значный восьмеричный код, 
# сохраняя ведущие нули.

import math
def area_right_triangle(leg1, leg2):
    """
    Считает площадь прямоугольного треугольника.
    S = (a * b) / 2
    """
    return 0.5 * leg1 * leg2

def area_heron(s1, s2, s3):
    """
    Считает площадь произвольного треугольника по формуле Герона.
    """
    p = (s1 + s2 + s3) / 2
    val = p * (p - s1) * (p - s2) * (p - s3)
    
    if val < 0:
        return 0  # Защита от невозможного треугольника
    return math.sqrt(val)

#ЗАДАЧА 1: ЧЕТЫРЕХУГОЛЬНИ
def solve_quadrilateral():
    print("\n=== ЧАСТЬ 1: Площадь четырехугольника ===")
    print("Даны стороны X, Y, Z, T. Угол между X и Y прямой.")
    
    try:
        x = float(input("Сторона X: "))
        y = float(input("Сторона Y: "))
        z = float(input("Сторона Z: "))
        t = float(input("Сторона T: "))
        
        # 1. Считаем площадь первой части (прямоугольный треугольник X, Y)
        s1 = area_right_triangle(x, y)
        
        # 2. Находим диагональ (гипотенузу), которая соединяет концы X и Y
        # d^2 = x^2 + y^2
        diagonal = math.sqrt(x**2 + y**2)
        
        # 3. Считаем площадь второй части (треугольник Z, T, Diagonal)
        # Используем формулу Герона
        s2 = area_heron(z, t, diagonal)
        
        total_area = s1 + s2
        
        print(f"\nПлощадь прямоугольного треугольника (X,Y): {s1:.2f}")
        print(f"Длина диагонали: {diagonal:.2f}")
        
        if s2 == 0:
            print("Внимание: Второй треугольник (Z, T, диагональ) построить невозможно.")
        else:
            print(f"Площадь второго треугольника (Z,T,Diag): {s2:.2f}")
            print(f"--> ОБЩАЯ ПЛОЩАДЬ: {total_area:.2f}")
            
    except ValueError:
        print("Ошибка: введите числа.")

