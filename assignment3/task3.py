# Задание 3.
# 1. Даны катеты двух прямоугольных треугольников. 
# Напишите функцию для вычисления длины гипотенузы этих треугольников. 
# Сравните и определите, какая из гипотенуз больше, а какая меньше.
# 2. Преобразуйте строку так, чтобы буквы каждого слова в ней были отсортированы в алфавитном порядке.

import math

# === ИНСТРУМЕНТЫ (ФУНКЦИИ) ===

def get_hypotenuse(leg1, leg2):
    """
    Считает гипотенузу по теореме Пифагора.
    Корень из (катет1^2 + катет2^2)
    """
    return math.sqrt(leg1**2 + leg2**2)

def sort_letters_in_sentence(sentence):
    """
    Принимает строку, сортирует буквы внутри каждого слова
    и возвращает новую строку.
    """
    # 1. Разбиваем предложение на список слов
    # "hello world" -> ["hello", "world"]
    words = sentence.split()
    
    processed_words = [] # Сюда будем складывать готовые слова
    
    for word in words:
        # 2. Сортируем буквы в слове
        # sorted("python") вернет список букв: ['h', 'n', 'o', 'p', 't', 'y']
        sorted_chars = sorted(word)
        
        # 3. Склеиваем буквы обратно в слово
        # "".join(...) берет список букв и делает из них строку
        new_word = "".join(sorted_chars)
        
        # Добавляем в наш список готовых слов
        processed_words.append(new_word)
    
    # 4. Склеиваем слова обратно в предложение с пробелами
    return " ".join(processed_words)


# === ЗАДАЧИ ===

def task_triangles():
    print("\n--- Сравнение гипотенуз ---")
    
    # Треугольник 1
    print("Треугольник №1:")
    a1 = float(input("  Катет 1: "))
    b1 = float(input("  Катет 2: "))
    hyp1 = get_hypotenuse(a1, b1) # Вызов функции
    
    # Треугольник 2
    print("Треугольник №2:")
    a2 = float(input("  Катет 1: "))
    b2 = float(input("  Катет 2: "))
    hyp2 = get_hypotenuse(a2, b2) # Вызов функции
    
    print(f"\nГипотенуза 1: {hyp1:.2f}")
    print(f"Гипотенуза 2: {hyp2:.2f}")
    
    # Логика сравнения
    if hyp1 > hyp2:
        print("РЕЗУЛЬТАТ: Первый треугольник больше.")
    elif hyp2 > hyp1:
        print("РЕЗУЛЬТАТ: Второй треугольник больше.")
    else:
        print("РЕЗУЛЬТАТ: Гипотенузы равны.")

def task_string_sort():
    print("\n--- Сортировка букв в словах ---")
    text = input("Введите предложение на английском: ")
    
    result = sort_letters_in_sentence(text)
    
    print(f"Результат: {result}")

# === ГЛАВНЫЙ ПУЛЬТ ===
def main():
    task_triangles()
    task_string_sort()

if __name__ == "__main__":
    main()
